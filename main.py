def tree_two(x,y):
    tree = trtl.Turtle()
    tree.setheading(90)
    tree.pensize(10)
    tree.pencolor(84,44,2)
    tree.penup()
    tree.goto(x,y)
    tree.pendown()
    for i in range(5):
        temp_heading = 90-tree.heading()
        tree.forward(50/(i+1))
        tree.pensize(3)
        tree.left(50-(i*3)+temp_heading)
        tree.forward(40/((i+1)/(i+0.5)))
        tree.color(15,122,5)
        tree.begin_fill()
        tree.right(50)
        tree.circle(15)
        tree.left(50)
        tree.end_fill()
        tree.penup()
        tree.color(84,44,2)
        tree.right(180)
        tree.forward(40/((i+1)/(i+0.5)))
        tree.left(180-(50-(i*3))+temp_heading)
        tree.pendown()
        tree.right(50-(i*3)-temp_heading)
        tree.forward(40/((i+1)/(i+0.5)))
        tree.color(15,122,5)
        tree.begin_fill()
        tree.right(180)
        tree.circle(15)
        tree.right(180)
        tree.end_fill()
        tree.color(84,44,2)
        tree.penup()
        tree.left(180)
        tree.forward(40/((i+1)/(i+0.5)))
        tree.right(180-(50-(i*3))-temp_heading)
        tree.right(10)
        tree.pendown()
        tree.pensize(10)
    tree.color(84,44,2)
    tree.left(170)
    tree.pensize(3)
    tree.forward(15)
    tree.color(15,122,5)
    tree.begin_fill()
    tree.circle(12)
    tree.end_fill()
    tree.penup()
    tree.goto(x-20, y + 100)
    tree.pendown()
    tree.begin_fill()
    tree.circle(13)
    tree.end_fill()
    tree.penup()
    tree.goto(x, y+110)
    tree.pendown()
    tree.begin_fill()
    tree.circle(12)
    tree.end_fill()
    tree.penup()
    tree.goto(x+10, y+90)
    tree.pendown()
    tree.begin_fill()
    tree.circle(16)
    tree.end_fill()

tree_two(300, -200)



